    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!"".equals(jTxtNomeTransportador.getText())) {
            String idTransportador = jTxtIdTransportador.getText();
            String nomeTransportador = jTxtNomeTransportador.getText();
            String chave = jTxtChave.getText();

            String sql = "UPDATE conhecimentos "
                    + "SET cod_transportador = '" + idTransportador + "', "
                    + "nome_transportador = '" + nomeTransportador + "' "
                    + "WHERE chave = '" + chave + "' ;";

            if (cn.conecta()) {
                try {
                    if (cn.executeAtualizacao(sql)) {
                        JOptionPane.showMessageDialog(this, "Transportador atualizado com sucesso!", "Atualização de Conhecimentos", JOptionPane.INFORMATION_MESSAGE);
                        desabilitaEdicao();
                    } else {
                        JOptionPane.showMessageDialog(this, "Não foi possível atualizar o transportador!", "Atualização de Conhecimentos", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e);
                } finally {
                    cn.desconecta();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Não há dados a serem atualizados!", "Atualização de Conhecimentos", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


