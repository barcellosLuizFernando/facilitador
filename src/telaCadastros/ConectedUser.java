    private void montaLista() {
        DefaultTableModel lista = (DefaultTableModel) jTable1.getModel();

        lista.setRowCount(0);
        lista.setColumnCount(0);

        lista.addColumn("Id");
        lista.addColumn("User");
        lista.addColumn("Host");
        lista.addColumn("db");
        lista.addColumn("Command");
        lista.addColumn("Time");
        lista.addColumn("State");
        lista.addColumn("Info");

        if (cn.iniciarTransacao()) {
            try {
                cn.executeConsulta("show processlist;");
                while (cn.rs.next()) {
                    lista.addRow(new String[]{
                        cn.rs.getString("id"),
                        cn.rs.getString("user"),
                        cn.rs.getString("host"),
                        cn.rs.getString("db"),
                        cn.rs.getString("command"),
                        cn.rs.getString("time"),
                        cn.rs.getString("state"),
                        cn.rs.getString("info"),});
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                cn.finalizarTransacao();
            }
        }

    }

    private void repetidor() {
        new Thread() {
            public void run() {

                try {
                    while (true) {

                        montaLista();

                        Thread.sleep(1000);
                    }
                } catch (InterruptedException e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }.start();
    }

